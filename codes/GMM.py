import numpy as np
import matplotlib.pyplot as plt
from sklearn.mixture import GaussianMixture
n=10000

#same mean
mu1=[2, 2, 2]
sigma1=[1, 0.4, 0.2]
p_i1=[0.1, 0.5, 0.4]
data=[]
x=[]
y=[]
np.random.seed(2)
for i in range(n):
    z_i=np.argmax(np.random.multinomial(1, p_i1))
    x_i=np.random.normal(mu1[z_i], sigma1[z_i])
    y_i=np.random.normal(mu1[z_i], sigma1[z_i])
    x.append(x_i)
    y.append(y_i)
    data.append([x_i,y_i])
gmm=GaussianMixture(n_components=3, random_state=0).fit(data)
labels = gmm.predict(data)
plt.scatter(x,y,c=labels, s=40, cmap='viridis')
plt.title('Same Mean and Different Variance')
plt.xlabel('X')
plt.ylabel('Y')
plt.legend()
plt.show()


